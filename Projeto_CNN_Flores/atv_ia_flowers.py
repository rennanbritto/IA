import tensorflow as tf
import numpy as np
import matplotlib.pyplot as plt
import seaborn as sns

from sklearn.metrics import (
    accuracy_score,
    precision_score,
    recall_score,
    f1_score
)

DATASET_PATH = "flower_photos"

"""ITEM A) Implemente no algoritmo a divisão de dados 70% para treino, 15%
para validação e 15% para teste
"""

# ITEM A)
# conjunto de treino (70%)
train_ds = tf.keras.utils.image_dataset_from_directory(
    DATASET_PATH,
    validation_split=0.30,
    subset="training",
    seed=42,
    image_size=(224,224),
    batch_size=32
)

# conjunto temporário (30%)
temp_ds = tf.keras.utils.image_dataset_from_directory(
    DATASET_PATH,
    validation_split=0.30,
    subset="validation",
    seed=42,
    image_size=(224,224),
    batch_size=32
)

# 15% pra teste e 15% para validação

val_size = len(temp_ds) // 2

val_ds = temp_ds.take(val_size)

test_ds = temp_ds.skip(val_size)

# mostra as classes
class_names = train_ds.class_names

print(class_names)

# desempenho
AUTOTUNE = tf.data.AUTOTUNE

train_ds = train_ds.prefetch(AUTOTUNE)
val_ds = val_ds.prefetch(AUTOTUNE)
test_ds = test_ds.prefetch(AUTOTUNE)

"""B) Implemente um dos modelos de rede neural convolucional CNN
(escolha um modelo), podendo ser uma ResNet50, InceptionNet,
XCeptionNet, EfficientNet, VGGNet, DenseNet, ShuffleNetV2,
SquezzeNet, InceptionResNetV2, SENetResNet ou MobileNet.
"""

# MOBILENETV2
base_model = tf.keras.applications.MobileNetV2(
    input_shape=(224,224,3),
    include_top=False,
    weights='imagenet'
)

base_model.trainable = False

model = tf.keras.Sequential([
    tf.keras.layers.Rescaling(1./255),

    base_model,

    tf.keras.layers.GlobalAveragePooling2D(),

    tf.keras.layers.Dense(
        128,
        activation='relu'
    ),

    tf.keras.layers.Dropout(0.3),

    tf.keras.layers.Dense(
        5,
        activation='softmax'
    )
])

model.compile(
    optimizer='adam',
    loss='sparse_categorical_crossentropy',
    metrics=['accuracy']
)

#Épocas
import time #calcula tbm o tempo
inicio = time.time()
history = model.fit(
    train_ds,
    validation_data=val_ds,
    epochs=10
)
fim = time.time()

"""ITEM C) (0,1) Implemente as métricas de classificação acurácia, precisão,
revocação (do inglês, reccal) e pontuação-F1 (do inglês, F1-score). O
algoritmo deve exibir na tela essas quatro métricas para serem colocadas
no arquivo .pdf como resultado.
"""

y_true = []
y_pred = []

for images, labels in test_ds:

    pred = model.predict(
        images,
        verbose=0
    )

    y_true.extend(labels.numpy())

    y_pred.extend(
        np.argmax(pred, axis=1)
    )

#acurácia
acc = accuracy_score(
    y_true,
    y_pred
)

print("Accuracy =", acc)

#Precisão
prec = precision_score(
    y_true,
    y_pred,
    average='weighted'
)

print("Precision =", prec)

#Recall
rec = recall_score(
    y_true,
    y_pred,
    average='weighted'
)

print("Recall =", rec)

#f1 score
f1 = f1_score(
    y_true,
    y_pred,
    average='weighted'
)

print("F1 Score =", f1)

