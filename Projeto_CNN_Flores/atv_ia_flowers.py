"""g) (0,1) Implemente um gráfico de colunas verticais exibindo o valor numérico da quantidade de acertos e erros com os respectivos valores numéricos percentuais por classe, tendo como referência de acertos e erros por classe os rótulos verdadeiros das imagens do conjunto de teste. Da mesma forma, o algoritmo deve exibir na tela o gráfico de colunas verticais que deve ser colocada no arquivo .pdf como parte do resultado."""

import numpy as np
import pandas as pd

acertos = []
erros = []

perc_acertos = []
perc_erros = []

for classe in range(len(class_names)):

    # imagens da classe verdadeira
    mask = (y_true == classe)

    total = np.sum(mask)

    corretos = np.sum(
        (y_true == classe) &
        (y_pred == classe)
    )

    incorretos = total - corretos

    acertos.append(corretos)
    erros.append(incorretos)

    perc_acertos.append(
        100 * corretos / total
    )

    perc_erros.append(
        100 * incorretos / total
    )

resultado = pd.DataFrame({
    "Classe": class_names,
    "Acertos": acertos,
    "% Acertos": np.round(perc_acertos,2),
    "Erros": erros,
    "% Erros": np.round(perc_erros,2)
})

print(resultado)
print()


